// Get subjects for a specific department and semester
export const getSubjectsByDepartmentAndSemester = (department, semester) => {
  if (!department || !semester) return [];
  
  const deptSubjects = DEPARTMENT_SUBJECTS[department];
  if (!deptSubjects) {
    console.warn('Department not found:', department, 'Available:', Object.keys(DEPARTMENT_SUBJECTS));
    return [];
  }
  
  const subjects = deptSubjects[semester] || [];
  // Filter out empty strings and undefined values
  return subjects.filter(subj => subj && subj.trim() !== '');
};

// Get all unique subjects for a department
export const getAllSubjectsByDepartment = (department) => {
  if (!department) return [];
  
  const deptSubjects = DEPARTMENT_SUBJECTS[department];
  if (!deptSubjects) return [];
  
  const allSubjects = new Set();
  Object.values(deptSubjects).forEach(subjects => {
    subjects.forEach(subject => allSubjects.add(subject));
  });
  return Array.from(allSubjects).sort();
};

// Check if a subject belongs to a department and semester
export const isSubjectInDepartmentAndSemester = (subject, department, semester) => {
  const subjects = getSubjectsByDepartmentAndSemester(department, semester);
  return subjects.includes(subject);
};

// Get all available departments
export const getDepartments = () => {
  return Object.keys(DEPARTMENT_SUBJECTS);
};



